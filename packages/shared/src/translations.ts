export function t(key: keyof typeof translations.pt, lang: Language = 'pt', params?: Record<string, string | number>): string {
  let text = translations[lang]?.[key] || translations.pt[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}

